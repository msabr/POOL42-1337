#include <stdio.h>

int	ft_sqrt(int nb)
{
	int		n;

	if ((nb < 0) || (nb > 2147483647))
		return (0);
	{
		if (nb == 1)
			return (1);
		n = 2;
		while (n < nb)
		{
			if ((n * n) == nb)
				return (n);
			n++;
		}
		return (0);
	}
	return (0);
}
