int	ft_iterative_factorial(int nb)
{
	int		f;
	int		i;

	f = 1;
	i = 0;
	if (nb < 0)
		return (0);
	while (i < nb)
	{
		f = f * (nb - i);
		i++;
	}
	return (f);
}
