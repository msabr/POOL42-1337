int	max(int* tab, unsigned int len)
{
	if (tab[0] == '\0')
	   return 0;

	int max;
	int i;
	i = 0;
	max = tab[0];
	while	(tab[i])
	{
		if (tab[i] > max)
			max = tab[i];
		i++;
	}
	return (max);
}
